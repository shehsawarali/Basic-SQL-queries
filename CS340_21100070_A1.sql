FROM CENSUS_DATA 
INNER JOIN SCHOOLS ON CENSUS_DATA.COMMUNITY_AREA_NUMBER = SCHOOLS.COMMUNITY_AREA_NUMBER 
WHERE SAFETY_SCORE = 1


